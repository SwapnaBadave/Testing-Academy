//even or odd number
let  Number= 7;
if (Number % 2 === 0) {
    console.log("The number is even.");
} else {
    console.log("The number is odd.");
}


// explaination: here we are using modulus 
// operator % to check if the number is even or odd. 
// if the number is divisible by 2 (i.e., the remainder is 0), 
// then it is an even number. if the number is not divisible by 2 (i.e., the remainder is 1), then it is an odd number. in this case, since 7 % 2 equals 1, 
// it will print "The number is odd."