// Identifier naming style examples in JavaScript
var firstName = "swapna";   // camelCase: common for variables and function names
var lastName = "Badave";    // camelCase with multiple words
var first_name = "swapna";  // snake_case: underscores between words
var FirstName = "swapna";   // PascalCase: often used for constructor or class names
var $firstName = "swapna";  // $ prefix is allowed and used in some libraries
var _firstName = "swapna";  // _ prefix is allowed and often used for private/internal variables
var firstName1 = "swapna";  // letters followed by digits is allowed

