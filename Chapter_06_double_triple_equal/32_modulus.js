//modulus operator
let result = 10 % 3; // modulus operator gives the remainder of the division
console.log(result); // Output: 1

console.log(10 % 2); // Output: 0 (10 is divisible by 2)
console.log(15 % 2); // Output: 1 (15 divided by 2 gives a quotient of 7 and a remainder of 1

// n%2 is a common way to check if a number n is even or odd. 
// If n % 2 equals 0, then n is even; if n % 2 equals 1,
//  then n is odd.