let number =[1,2,3]
number.push(4); // adds 4 to the end of the array
console.log(number); // Output: [1, 2, 3, 4]    
number.unshift(0); // adds 0 to the beginning of the array
console.log(number); // Output: [0, 1, 2, 3, 4]
number.pop(); // removes the last element (4) from the array
console.log(number); // Output: [0, 1, 2, 3]
number.shift(); // removes the first element (0) from the array
console.log(number); // Output: [1, 2, 3]
let array =[1,2,3]
array.shift(); // removes the first element (1) from the array
console.log(array); // Output: [2, 3]
array.unshift(0); // adds 0 to the beginning of the array
console.log(array); // Output: [0, 2, 3]
