// Valid JavaScript identifiers:
var $ = 30;         // starts with $
var _ab = 10;       // starts with _
var aB123 = 20;     // starts with a letter, contains digits
var ABC = 20;       // uppercase letters are allowed
var myVar = "ok";  // camel case identifier

// Invalid JavaScript identifiers (commented out because they would cause syntax errors):
// var 123a = 10;    // cannot start with a digit
// var var-name = 15; // hyphens are not allowed
// var %percent = 5;  // cannot start with %
// var true = false;  // reserved keyword cannot be used as an identifier

// Notes:
// - Identifiers can start with letters, $, or _
// - After the first character, digits are allowed too
// - Keywords and reserved words cannot be used as variable names

